export const salesPlanViewPerformanceMonth = pgTable(
  "sales_plan_view_performance_month",
  {
    id: uuid("id").primaryKey().defaultRandom(),
    workspaceId: uuid("workspace_id").notNull(),
    planId: uuid("plan_id")
      .notNull()
      .references(() => salesPlanViewPerformance.id, { onDelete: "cascade" }),
    ym: varchar("ym", { length: 6 }).notNull(),
    serOrderAmt: numeric("ser_order_amt"),
    prdOrderAmt: numeric("prd_order_amt"),
    infOrderAmt: numeric("inf_order_amt"),
    servAmt: numeric("serv_amt"),
    prodAmt: numeric("prod_amt"),
    inManMonth: numeric("in_man_month", { precision: 15, scale: 10 }),
    outManMonth: numeric("out_man_month", { precision: 15, scale: 10 }),
    dirInAmt: numeric("dir_in_amt"),
    dirOutAmt: numeric("dir_out_amt"),
    indirOrgAmt: numeric("indir_org_amt"),
    indirAllAmt: numeric("indir_all_amt"),
    sgaAmt: numeric("sga_amt"),
    expAmt: numeric("exp_amt"),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow().notNull(),
    updatedAt: timestamp("updated_at", { withTimezone: true }),
    createdBy: uuid("created_by"),
    updatedBy: uuid("updated_by"),
  },
  (t) => ({
    planYmUniq: uniqueIndex("sales_plan_view_perf_month_plan_ym_uniq").on(t.planId, t.ym),
    wsYmIdx: index("sales_plan_view_perf_month_ws_ym_idx").on(t.workspaceId, t.ym),
  }),
);

export const salesPlanAcl = pgTable(
  "sales_plan_acl",
  {
    planId: uuid("plan_id")
      .notNull()
      .references(() => salesPlanViewPerformance.id, { onDelete: "cascade" }),
    userId: uuid("user_id")
      .notNull()
      .references(() => user.id, { onDelete: "cascade" }),
    canRead: boolean("can_read").default(true).notNull(),
    canWrite: boolean("can_write").default(false).notNull(),
    createdAt: timestamp("created_at", { withTimezone: true }).defaultNow().notNull(),
  },
  (t) => ({
    pk: primaryKey({ columns: [t.planId, t.userId] }),
  }),
);
