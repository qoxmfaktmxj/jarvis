/**
 * Contract-adjacent sales screens.
 *
 * - salesContractUpload: TBIZ037 planning/view/performance upload rows.
 * - salesPlanViewPerformance: TBIZ050 plan/view/performance master rows.
 * - salesPlanViewPerformanceMonth: TBIZ051 monthly details.
 * - salesPlanAcl: Jarvis row ACL for plan/view/performance master rows.
 */

import {
  boolean,
  index,
  numeric,
  pgTable,
  primaryKey,
  text,
  timestamp,
  uniqueIndex,
  uuid,
  varchar,
} from "drizzle-orm/pg-core";
import { user } from "./user.js";

